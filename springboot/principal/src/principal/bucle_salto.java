package principal;

//Instrucciones de salto - Ejercicio 20
class bucle_salto
{
		public static void main(String args[])
		{
			int x = 1;

     // Salir cuando x llega a ser menor o igual que tres
		while (x <= 3)
		{
         System.out.println("Valor de x: " + x);

         //incrementa el valor de x para la siguiente iteración
         x++;
		}
	}
}